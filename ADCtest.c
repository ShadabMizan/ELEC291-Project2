void ssd1309_draw_canvas(const uint8_t *canvas) {
    for (uint8_t page = 0; page < 8; page++) {
        ssd1309_set_pos(0, page);
        OLED_CS_LO(); OLED_DC_HI();
        for (uint8_t col = 0; col < 128; col++) {
            ssd1309_write_byte(pgm_read_byte(&canvas[page * 128 + col]));
        }
        OLED_CS_HI();
    }
}

