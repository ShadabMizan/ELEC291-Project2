static void ssd1309_write_byte(uint8_t byte) {
    for (uint8_t i = 0; i < 8; i++) {
        if (byte & 0x80) OLED_DIN_HI(); else OLED_DIN_LO();
        OLED_CLK_HI();
        OLED_CLK_LO();
        byte <<= 1;
    }
}

static void ssd1309_cmd(uint8_t cmd) {
    OLED_CS_LO(); OLED_DC_LO();
    ssd1309_write_byte(cmd);
    OLED_CS_HI();
}

static void ssd1309_data(uint8_t data) {
    OLED_CS_LO(); OLED_DC_HI();
    ssd1309_write_byte(data);
    OLED_CS_HI();
}

void ssd1309_init(void) {
    DDRB |= (1<<CLK) | (1<<DIN);
    DDRD |= (1<<DC) | (1<<CS) | (1<<RST);
    OLED_CS_HI(); OLED_RST_HI(); _delay_ms(1);
    OLED_RST_LO(); _delay_ms(10);
    OLED_RST_HI(); _delay_ms(300);

    ssd1309_cmd(0xAE); // display off
    ssd1309_cmd(0x20); ssd1309_cmd(0x02); // Page addressing mode
    ssd1309_cmd(0xA1); ssd1309_cmd(0xC8);
    ssd1309_cmd(0xAF); // display on
}

void ssd1309_clear(void) {
    for (uint8_t p = 0; p < 8; p++) {
        ssd1309_cmd(0xB0 | p);
        ssd1309_cmd(0x00); ssd1309_cmd(0x10);
        for (uint8_t i = 0; i < 128; i++) ssd1309_data(0x00);
    }
}

void ssd1309_set_pos(uint8_t col, uint8_t page) {
    ssd1309_cmd(0xB0 | (page & 0x07));
    ssd1309_cmd(0x00 | (col & 0x0F));
    ssd1309_cmd(0x10 | ((col >> 4) & 0x0F));
}

void ssd1309_print(uint8_t col, uint8_t page, char *str) {
    while (*str) {
        if (col > 122) break;
        ssd1309_set_pos(col, page);
        uint8_t c = *str;
        if (c < 32 || c > 90) c = 32;
        uint8_t idx = c - 32;
        for (uint8_t i = 0; i < 5; i++) ssd1309_data(FONT_5x7[idx][i]);
        ssd1309_data(0x00);
        col += 6;
        str++;
    }
}

volatile unsigned int toggle_flag = 0;

void adc_init(void) {
    ADMUX = (1<<REFS0);
    ADCSRA = (1<<ADEN)|(1<<ADPS2)|(1<<ADPS1)|(1<<ADPS0);
}

uint16_t adc_read(int pin) {
    ADMUX = (ADMUX & 0xF8) | (pin & 0x07);
    ADCSRA |= (1<<ADSC);
    while (ADCSRA & (1<<ADSC));
    return (ADCW);
}

void SendByte(unsigned char c) {
    toggle_flag = 1; _delay_us(1E6/sBAUD);
    for (int i = 0; i < 8; i++) {
        toggle_flag = (c & 1) ? 0 : 1;
        c >>= 1; _delay_us(1E6/sBAUD);
    }
    toggle_flag = 0; _delay_us(1E6/sBAUD);
}

ISR(TIMER1_COMPA_vect) {
    if (toggle_flag) PINB = (1 << PB1);
    else PORTB &= ~(1 << PB1);
}  

